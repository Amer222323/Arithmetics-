public class Arthmitics {
    public int  sum(int x,int y){
        return x+y;
    }
    public int sub(int x,int y){
        return x-y;
    }
    public int mult(int x,int y){
        return x*y;
    }
    public int div(int x,int y){
        return x/y;
    }
    public int modulo(int x, int y){
        return x%y;
    }
}
